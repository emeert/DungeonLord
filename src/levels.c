int current_level;
int next_level;


void load_next_level()
{
    if (current_level < LEVELS - 1)
    {
        current_level++;  // Move to the next level
        next_level = 1;
    }
    else
    {
        printf("Congratulations! You completed all levels!\n");
    }
}



